package day17_While_DoWhileLoop;

public class DoWhileLoop {

    public static void main(String[] args) {

        boolean a = false;

        while (a){
            System.out.println("Wooden Spoon 1");
        }

        System.out.println("---------------------------------");
        do {
            System.out.println("Wooden Spoon 2");
        }while(a);

    }
}
