package day04_Varibles;

public class EmployeeInfo {

    public static void main(String[] args) {

        String name = "Donald Trump";
        int age = 99;
        char gander = 'M';
        String company = "Trump corporation";
        String jobTitle = "President";
        double yearsOfExpereince = 77.7;
        int salary = 100;
        boolean isFullTime = true;
        boolean isMarried = false;
        String employeeId = "A1";
        String SSN = "(000)-00-0001";

        System.out.println("name = " + name);
        System.out.println("employeeId = " + employeeId);
        System.out.println("gander = " + gander);
        System.out.println("age = " + age);
        System.out.println("jobTitle = " + jobTitle);
        System.out.println("company = " + company);
        System.out.println("yearsOfExpereince = " + yearsOfExpereince);
        System.out.println("salary = " + salary);
        System.out.println("SSN = " + SSN);
        System.out.println("isMarried = " + isMarried);

    }
}

